<?php 
    if (isset($_POST['kirim'])) {
        $bil1 = $_POST['bil1'];
        $bil2 = $_POST['bil2'];
        $aksi = $_POST['aksi'];

        switch ($aksi) {
            case 'tambah':
                $output = $bil1 + $bil2;
                break;
            case 'kurang':
                $output = $bil1 - $bil2;
                break;
            case 'kali':
                $output = $bil1 * $bil2;
                break;
            case 'bagi':
                $output = $bil1 / $bil2;
                break;            
        }
    }
?>

    <form method="POST">
        <div class="kal">
        <h1>Kalkulator Sederhana</h1>
        <hr>
        <p>Bilangan Pertama :</p>
        <input class="FormInput" type="number" name="bil1" value="0">
        <p>Bilangan Kedua :</p>
        <input class="FormInput" type="number" name="bil2" value="0">
        <p>Aksi</p>
        <select class="FormSelect" name="aksi">
            <option value="tambah">Tambah (+)</option>
            <option value="kurang">Kurang (-)</option>
            <option value="kali">Kali (X)</option>
            <option value="bagi">Bagi (/)</option>
        </select>
        <button type="submit" name="kirim">Hitung</button>
        <?php 
            if (empty($output)) {
                $hasil = 0;
            } else {
                $hasil = $output;
            }
			
         ?>
        <input class="FormHasil" type="number" value="<?= $hasil ?>" readonly>
    </div>
    </form>
